<?php
    //FOR
    for($i = 0; $i < 5; $i++){ //tambien $i = $i + 1
        echo "$i <br>";
    }
    
    //WHILE
    $i = 0;
    while($i < 5){
        echo "$i <br>";
        $i = $i + 1;
    }

    //DO WHILE 
    $i = 0;
    do{
        echo "$i <br>";
        $i = $i +1;
    }while($i < 5);
?>